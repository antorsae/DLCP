#[derive(Serialize, Deserialize, Clone, Debug)]
pub struct PeripheralStubs {
    variant: Variant,
}

impl PeripheralStubs {
    pub fn new(variant: Variant) -> Self {
        PeripheralStubs { variant }
    }

    pub fn reset_state(&mut self) {}

    pub fn on_sfr_write(&mut self, addr: u16, _value: u8, mem: &mut Memory) {
        for policy in policies_for(self.variant) {
            if policy.addr != Some(addr) {
                continue;
            }
            match policy.behavior {
                StubBehavior::RegisterBackedNoSideEffects { writable_mask } => {
                    let value = mem.read_raw(Address::from_raw(addr)) & writable_mask;
                    mem.write_raw(Address::from_raw(addr), value);
                }
                StubBehavior::NotPresentOnDlcpPackage => {
                    mem.write_raw(Address::from_raw(addr), 0x00);
                }
            }
        }
    }
}

pub fn policies_for(variant: Variant) -> &'static [StubPolicy] {
    match variant {
        Variant::Pic18F25K20 => K20_POLICIES,
        Variant::Pic18F2455 => PIC2455_POLICIES,
    }
}
