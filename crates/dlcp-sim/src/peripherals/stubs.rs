//! Explicit policies for peripherals that are not behaviourally modelled.
//!
//! FID-15 keeps these out of the "accidental RAM byte" bucket.  If DLCP
//! firmware touches one of these SFRs, the simulator gives it deterministic
//! register storage and documented no-side-effect behavior instead of silently
//! implying that CCP waveforms, comparator analog thresholds, HLVD trips, FVR
//! settling, or PSP/SPP transfers are implemented.

use crate::memory::{Address, Memory, Variant};
use serde::{Deserialize, Serialize};

#[derive(Serialize, Deserialize, Copy, Clone, Debug, Eq, PartialEq)]
pub enum StubBehavior {
    /// Register is present and readable/writable through the listed mask, but
    /// no waveform, analog, voltage-trip, or interrupt side effects are
    /// generated by this stub.
    RegisterBackedNoSideEffects { writable_mask: u8 },
    /// The DLCP package/variant does not expose this peripheral; no SFR is
    /// owned by the simulator for it.
    NotPresentOnDlcpPackage,
}

#[derive(Serialize, Deserialize, Copy, Clone, Debug, Eq, PartialEq)]
pub struct StubPolicy {
    pub variant: Variant,
    pub peripheral: &'static str,
    pub sfr: &'static str,
    pub addr: Option<u16>,
    pub behavior: StubBehavior,
    pub citation: &'static str,
    pub dlcp_scope: &'static str,
}

